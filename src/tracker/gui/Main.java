    private void btn_IniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IniciarActionPerformed
        // TODO add your handling code here:
        int n=3;
        btn_Iniciar.setEnabled(false);
        String[] param=new String[5];
        param[0]=jComboPlataforma.getSelectedItem().toString();
        param[1]=jComboLogin.getSelectedItem().toString();
        param[2]=jComboContrasena.getSelectedItem().toString();
        param[3]=jComboPuertoDB.getSelectedItem().toString();
        param[4]=jComboServerDB.getSelectedItem().toString();
        //MYSQL     3
        //POSTGRES  4
        String manejador=jComboServidor.getSelectedItem().toString();
        
        if(manejador.equals("MySQL"))n=3;
        else if(manejador.equals("POSTGRES"))n=4;
        control=new Controller(param,n);
        control.conexion();
        control.sentenciaSistema();
    }//GEN-LAST:event_btn_IniciarActionPerformed

