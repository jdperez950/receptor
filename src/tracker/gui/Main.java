    private void selecionar(String selec){
        jComboContrasena.removeAllItems();
        jComboLogin.removeAllItems();
        jComboPuertoDB.removeAllItems();
        if(selec.equals("MySQL")){
            jComboContrasena.addItem("160819");
            jComboContrasena.addItem("");
            jComboLogin.addItem("ginei");
            jComboLogin.addItem("root");
            jComboPuertoDB.addItem("3306");
        }else if(selec.equals("POSTGRES")){
            jComboContrasena.addItem("admin");
            jComboLogin.addItem("postgres");
            jComboPuertoDB.addItem("5432");
        }
    }

